public class Number {

	public static void main(String[] args) {
		System.out.println(6+2); // 8
		System.out.println(6-2); // 4
		System.out.println(6*2); // 12
		System.out.println(6/2); // 3
		
		System.out.println(Math.PI); // pi = 3.141592...
		System.out.println(Math.floor(Math.PI)); // floor operation
		System.out.println(Math.ceil(Math.PI)); // ceiling operation
		
		// Math: a class containing operators, constants, etc
		// document: https://docs.oracle.com/javase/8/docs/api/java/lang/Math.html

	}

}
